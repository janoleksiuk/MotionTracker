/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_FATFS_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM8_Init();
  /* USER CODE BEGIN 2 */

  HAL_Delay(1000); //SD card needs short delay to settle

   //variables for FatFs
   FATFS FatFs; 	//Fatfs handle
   FIL fil; 		//File handle
   FRESULT fres; //Result after operations

   //Open the file system

   fres = f_mount(&FatFs, "", 1); //1 = mount now
   if (fres != FR_OK)
   {
	    HAL_Delay(100);
	    HAL_NVIC_SystemReset(); // performing software system reset in case sd card is not detected correctly
   }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

   //1st Encoder setup:
   HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);

   //2nd Encoder setup:
   HAL_TIM_Encoder_Start(&htim4, TIM_CHANNEL_ALL);

   //3rd Encoder setup:
   HAL_TIM_Encoder_Start(&htim1, TIM_CHANNEL_ALL);

   //4th Encoder setup:
   HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);

   //5th Encoder setup:
   HAL_TIM_Encoder_Start(&htim8, TIM_CHANNEL_ALL);

   //encoder variables
   int tim  = HAL_GetTick();
   int t = 0;

    //Create text file to store data from encoders:
    fres = f_open(&fil, "dane.txt", FA_WRITE | FA_OPEN_ALWAYS | FA_CREATE_ALWAYS);
    f_printf(&fil, "START\n");

    // main loop
    while (1)
    {

      //acquiring time and encoder values
  	  t  = HAL_GetTick() - tim;
  	  int16_t e1 = __HAL_TIM_GET_COUNTER(&htim3);
  	  int16_t e2 = __HAL_TIM_GET_COUNTER(&htim4);
  	  int16_t e3 = __HAL_TIM_GET_COUNTER(&htim1);
  	  int16_t e4 = __HAL_TIM_GET_COUNTER(&htim2);
  	  int16_t e5 = __HAL_TIM_GET_COUNTER(&htim8);

  	  //writing values as row separated with spaces in file in sd card
	  f_printf(&fil, "%d%6d%6d%6d%6d%6d\n", t, e1, e2, e3, e4, e5);

	  //writing values using USART - uncomment if using cable connection with PC
	  //printf("%d,%d,%d,%d,%d,%d\r\n", t, e1, e2, e3, e4, e5);

	  HAL_Delay(10);

	  // if button pushed - closing sd card flow
	  if (button_pushed)
	  {
		f_close(&fil);
		f_mount(NULL, "", 0);
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    }

  /* USER CODE END 3 */
}

